class Refs(TypedDict):
    """Format of reference values."""

    q: Tensor
    """EEQ charges."""

    gw: Tensor
    """
    Gaussian weights. Shape must be ``(nrefs, nat)``, which we have to
    transpose for comparison to the Python-calculated tensor later.
    """

    gw_d4s: Tensor
    """
    Gaussian weights for D4s. Shape must be ``(nrefs, nat)``, which we have
    to transpose for comparison to the Python-calculated tensor later.
    """

    c6: Tensor
    """
    Reference C6 coefficients of shape is ``(nat, nat)``.
    """

    c6_d4s: Tensor
    """
    Reference C6 coefficients for D4S of shape is ``(nat, nat)``.
    """


